export interface Character {
  id: string;
  name: string;
  avatar: string;
  description: string;
  creator?: string;
}

export interface WikiEntry {
  id: string;
  title: string;
  content: string;
  subsections?: { title: string; content: string }[];
}

export interface StorylineItem {
  id: string;
  title: string;
  englishTitle?: string;
  description: string;
  quote?: string;
  relatedCharacters: string[];
}

export interface StorylineData {
  items: StorylineItem[];
  disabled?: boolean;
  disabledMessage?: string;
}

export interface WorldSetting {
  entries: WikiEntry[];
}

export interface World {
  id: string;
  name: string;
  subtitle?: string;
  tagline: string;
  heroImage: string;
  accentColor: string;
  setting: WorldSetting | null;
  storylines: StorylineData | null;
  characters: Character[];
  disabledTabs?: {
    setting?: boolean;
    storylines?: boolean;
    characters?: boolean;
  };
}

// 生成占位符角色
function generatePlaceholderCharacters(prefix: string, count: number): Character[] {
  return Array.from({ length: count }, (_, i) => ({
    id: `${prefix}-char-${i + 1}`,
    name: `角色${i + 1}`,
    avatar: `/placeholder-avatar-${i + 1}.jpg`,
    description: "角色信息正在补充中..."
  }));
}

